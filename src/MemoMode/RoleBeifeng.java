package MemoMode;

public class RoleBeifeng {

	private int xueLiang;
	private int ATC;
	private int JYZ;
	public int getXueLiang() {
		return xueLiang;
	}
	public void setXueLiang(int xueLiang) {
		this.xueLiang = xueLiang;
	}
	public int getATC() {
		return ATC;
	}
	public void setATC(int aTC) {
		ATC = aTC;
	}
	public int getJYZ() {
		return JYZ;
	}
	public void setJYZ(int jYZ) {
		JYZ = jYZ;
	}
	public RoleBeifeng(int xueLiang, int aTC, int jYZ) {
		super();
		this.xueLiang = xueLiang;
		ATC = aTC;
		JYZ = jYZ;
	}
	
}
